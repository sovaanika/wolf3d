#include "../wolf.h"

void	draw(t_wolf *w)
{
	int		y;
	int		x;

	y = -1;
	while (++y < w->sizey)
	{
		x = -1;
		while (++x < w->sizex)
		{
			w->data[x + y * w->wid + 100] = (w->map[y][x].z) * 122255;
		}
	}
	mlx_put_image_to_window(w->mlx_ptr, w->win_ptr, w->img_ptr, 100, 100);
}
