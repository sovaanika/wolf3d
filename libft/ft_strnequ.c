#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	i;

	i = 1;
	if (s1 != NULL && s2 != NULL)
	{
		while ((*s1 && *s2 && (*s1 == *s2)) && (i < n))
		{
			s1++;
			s2++;
			i++;
		}
		if (*s1 == *s2)
			return (1);
		else if (n == 0)
			return (1);
	}
	return (0);
}
